# Merging/Combining many lists into one

# You can merge many lists into one list by placing all the lists inside one list() function.

# Create a list
list1 <- list(1,2,3)

# Create another list
list2 <- list("Sun","Mon","Tue")

# Merge the two lists using c() function
merged.list <- c(list1,list2)


print(merged.list)