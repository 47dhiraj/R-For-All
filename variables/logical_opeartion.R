# Logical/Boolean opration in R


# Basic Example1:
result1 <- 47 < 50

typeof(result1)
result1



# Another Example2 (Not Operator):
result2 <- !result1
result2



# Example of | (i.e or) Operator
result3 <- result1 | result2
result3



# Example of  &  (i.e and) operator
result4 <- result1 & result2
result4


# Example of isTRUE() logical operation:
isTRUE(result3)
isTRUE(result4)

